    <h2>Taylor Swift</h2>
    <font size="3px" face="arial">
      <p>
        <img src="gambar/album4.jpg" width="10%" alt="">
        Taylor Alison Swift (lahir 13 Desember 1989) adalah seorang penyanyi-penulis lagu berkebangsaan Amerika Serikat.
        Penulisan lagu naratifnya, yang sering berpusat di sekitar kehidupan pribadinya, telah menerima pujian kritis
        dan liputan media yang luas. Lahir di West Reading, Pennsylvania,[4] Taylor Swift pindah ke Nashville, Tennessee
        pada usia 14 tahun untuk mengejar kariernya di musik country. Album studio debutnya yang bernama sama seperti
        namanya merupakan album tahun 2000-an terlama yang berada di Billboard 200. Single ketiga album, "Our Song",
        menjadikannya sebagai penyanyi termuda yang menulis sendiri dan membawakan sebuah lagu nomor satu di tangga lagu
        Billboard Hot Country Songs. Album studio kedua Swift, Fearless (2008), mengusung gaya country pop dan
        memenangkan Penghargaan Grammy sebagai Album Terbaik Tahun Ini. Didukung oleh kesuksesan single-single bergenre
        campuran seperti "Love Story" dan "You Belong with Me", Fearless disertifikasi Berlian oleh Recording Industry
        Association of America (RIAA). Speak Now, album studio ketiganya, percampuran country pop dengan perpaduan rock
        dan menghasilkan single-single 10 besar yaitu "Mine" dan "Back to December".
      </p>
      <p>
        Album studio keempat Taylor Swift, Red (2012), bereksperimen dengan aliran pop, rock, dan elektronik. Album ini
        menghasilkan single-single lima besar, "We Are Never Ever Getting Back Together", lagu nomor satu pertamanya di
        Billboard Hot 100, dan "I Knew You Were Trouble". Dengan album studio kelimanya, 1989 (2014), dia mengumumkan
        transisi penuhnya kedalam pop. Rekaman pop ini membuat Taylor Swift menjadi artis solo wanita pertama yang
        memenangkan Penghargaan Grammy kategori Album Tahun Ini sebanyak dua kali dan menghasilkan tiga lagu nomor satu
        di Billboard Hot 100—"Shake It Off", "Blank Space", dan "Bad Blood". Dia kembali memasukkan bunyi pop-elektronik
        dalam dua album studionya yang berikutnya: Reputation (2017), yang menggabungkan elemen dari hip hop dan
        menghasilkan single nomor satu di Billboard Hot 100, "Look What You Made Me Do", Lover (2019), yang merupakan
        album terlaris di dunia tahun 2019. Pada tahun 2023 salah satu lagu di album Lover yaitu "Cruel Summer" mendadak
        viral berkat The Era's Tour yang menjadikan lagu ini menduduki peringkat pertama Hot 100 setelah 4 tahun
        perilisannya, sekaligus menjadikan Taylor Swift memiliki singel hits nomor satu dalam 7 album berturut-turut
        ("Red", "1989", "reputation", "Lover", "folklore", "evermore", dan “Midnights"). Album studio kedelapan Taylor
        Swift yang beraliran indie folk dan rock alternatif, Folklore (2020), dan single utamanya, "Cardigan",
        membuatnya menjadi artis pertama yang memuncaki Billboard 200 dan Hot 100 secara bersamaan. Dan mengulang lagi
        kesuksesan dengan merilis Album studio kesembilan masih dengan indie folk dan rock alternatif, Evermore dengan
        hits no.1 "Willow".
      </p>
